package com.perennial.HIBday1;

public class Subjects {
	private String  name;
	private int internalMrks;
	private int externalMrks;
	private int totalMrks;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getInternalMrks() {
		return internalMrks;
	}
	public void setInternalMrks(int internalMrks) {
		this.internalMrks = internalMrks;
	}
	public int getExternalMrks() {
		return externalMrks;
	}
	public void setExternalMrks(int externalMrks) {
		this.externalMrks = externalMrks;
	}
	public int getTotalMrks() {
		return totalMrks;
	}
	public void setTotalMrks(int totalMrks) {
		this.totalMrks = totalMrks;
	}
	
	
	

}
